                <li>
                   <span class="tools">Electronic Tools</span>: Soldering iron is a very useful tool for assembling electronic 
                   circuits and connecting copper wires together. For electronic circuits For electronic circuits you'll need 
                   a light soldering iron with a small point. Soldering electronic components is done 
                   with "soft soldering": with a low temperature (less than 300°C). Usually for electronics the melting point of
                   the solder lays around 238°C. When buying solder choose for a solder wire with non-corrosive 
                   flux and take the thinnest wire you can find .
                   
                   For connecting metal wires you'll need something more powerful (30W-100W) like a
                   soldering pistol, but an ordinary soldering iron would do just as well. Copper is easy to solder and has a reasonable
                   strong bond whereas, aluminum has a weak bond. For stronger connections it's better to braze instead of soft soldering.
                   Brazing involves higher temperatures (typical between 450°C and 1000°C) and different
                   flux and solder (copper and zinc or silver alloys) it also requires a welding torch instead of a soldering iron.
                    <br/><p></p>
                </li>
                <li>
                   <span class="tools">BreadBoard</span>: The boards allow you to build a temporary circuit in no time. Especially handy for
                   testing new circuits. Connections are made with either ordinary thin stiff wire with the insulation removed at the ends or
                   with special breadboard wires with stronger tips. Wires with crocodile clamps are needed for hooking up signal generator, 
                   oscilloscope, DMM, etc. Larger boards have connectors (typically banana plugs) for the power supply.
                   <br/><p></p>
                </li>
                <li>
                    <span class= "tools">Connectors</span>: Assembling parallel ribbon cables from ribbon and the IDC connectors. When using 
                    connectors you’re using, remove them from the side furthest from the ink stripe so it’s still present on the cable.helps 
                    guide you when you’re mating the cable connectors with those on the equipment, knowing that the stripe corresponds with pin 1.
                    <br/><p></p>
                </li>
                <li>
                   <span class="tools">network connector</span>: These are the connectors used on UTP network cables. A smaller version (RJ11) is used
                   for telephones. You need a crimping tool to attach the connector to the cable. These connectors are very useful for hooking up different
                   PCB with each other. A good use for RJ45 connectors is for making serial (RS232) programming cables for small embedded systems. If you are
                   building small embedded controller boards an RJ45 can be a handy connector size to use.
                   <br/><p></p>
